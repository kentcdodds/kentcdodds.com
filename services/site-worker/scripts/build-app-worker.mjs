import { createRequire } from 'node:module'
import { mkdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import * as esbuild from 'esbuild'

const require = createRequire(import.meta.url)
const __dirname = path.dirname(fileURLToPath(import.meta.url))
const siteWorkerRoot = path.resolve(__dirname, '..')
const repoRoot = path.resolve(siteWorkerRoot, '../..')
const siteRoot = path.resolve(repoRoot, 'services/site')
const distDir = path.resolve(siteWorkerRoot, 'dist')
const stubsDir = path.resolve(siteWorkerRoot, 'src/dynamic/stubs')

const reactPublicExports = Object.keys(require('react'))
	.filter((name) => !name.startsWith('__'))
	.sort()
const jsxRuntimeExports = Object.keys(require('react/jsx-runtime')).sort()

const stubAliases = {
	bcrypt: path.join(stubsDir, 'bcrypt.ts'),
	'better-sqlite3': path.join(stubsDir, 'better-sqlite3.ts'),
	esbuild: path.join(stubsDir, 'esbuild.ts'),
	'@esbuild-plugins/node-resolve': path.join(
		stubsDir,
		'esbuild-node-resolve.ts',
	),
	'@mdx-js/esbuild': path.join(stubsDir, 'mdx-esbuild.ts'),
	'mdx-bundler/client/index.js': path.join(
		stubsDir,
		'mdx-bundler-client.ts',
	),
	'mdx-bundler': path.join(stubsDir, 'mdx-bundler.ts'),
	'reading-time': path.join(stubsDir, 'reading-time.ts'),
}

const sharedDefine = {
	'process.env.NODE_ENV': JSON.stringify('production'),
	'import.meta.env.MODE': JSON.stringify('production'),
	'import.meta.env.PROD': 'true',
	'import.meta.env.DEV': 'false',
	'import.meta.env.SSR': 'true',
	'import.meta.url': JSON.stringify('file:///worker/app-worker.js'),
}

async function ensureSiteBuild() {
	const siteBuildPath = path.join(siteRoot, 'build/server/index.js')
	try {
		await readFile(siteBuildPath)
	} catch {
		throw new Error(
			'Site server build missing. Run `npm run build:site --workspace site-worker` first.',
		)
	}
}

function generateReactShimSource() {
	const namedExports = reactPublicExports
		.map((name) => `export const ${name} = React.${name};`)
		.join('\n')
	return `// Generated by build-app-worker.mjs — do not edit.
const R = globalThis[Symbol.for('kentcdodds.sharedReact')];
const React = R?.default ?? R;
export default React;
${namedExports}
`
}

function generateJsxRuntimeShimSource() {
	const namedExports = jsxRuntimeExports
		.map((name) => `export const ${name} = J.${name};`)
		.join('\n')
	return `// Generated by build-app-worker.mjs — do not edit.
const J = globalThis[Symbol.for('kentcdodds.sharedJsxRuntime')];
${namedExports}
`
}

async function writeModuleMapShims() {
	await writeFile(
		path.join(distDir, 'react-shim.js.txt'),
		generateReactShimSource(),
	)
	await writeFile(
		path.join(distDir, 'react-jsx-runtime-shim.js.txt'),
		generateJsxRuntimeShimSource(),
	)
}

async function patchAppWorkerNodeRequires(outfile) {
	let contents = await readFile(outfile, 'utf8')
	const nodeBuiltins = [
		'assert',
		'buffer',
		'crypto',
		'events',
		'fs',
		'http',
		'https',
		'os',
		'path',
		'process',
		'stream',
		'string_decoder',
		'tty',
		'url',
		'util',
		'zlib',
	]
	const imports = nodeBuiltins.map(
		(mod) => `import * as __node_${mod}__ from 'node:${mod}';`,
	)
	const requireShimNames = new Set(['__require'])
	const shimMatch = contents.match(/(\w+)=\([^]*?Dynamic require of/)
	if (shimMatch?.[1]) requireShimNames.add(shimMatch[1])

	const replacements = nodeBuiltins.flatMap((mod) => {
		const target = `__node_${mod}__`
		return Array.from(requireShimNames).flatMap((shim) => [
			[`${shim}("${mod}")`, target],
			[`${shim}('${mod}')`, target],
			[`${shim}("node:${mod}")`, target],
			[`${shim}('node:${mod}')`, target],
		])
	})
	let changed = false
	for (const [from, to] of replacements) {
		if (contents.includes(from)) {
			contents = contents.replaceAll(from, to)
			changed = true
		}
	}
	if (!changed) return
	contents = `${imports.join('\n')}\n${contents}`
	await writeFile(outfile, contents)
}

async function patchAppWorkerWorkerdCompat(outfile) {
	let contents = await readFile(outfile, 'utf8')
	contents = contents.replaceAll(
		/globalThis\["__dirname"\] = path3\.dirname\(fileURLToPath3\([^)]+\)\);/g,
		'globalThis["__dirname"] = "/worker";',
	)
	await writeFile(outfile, contents)
}

async function buildAppWorkerBundle() {
	const bootstrapPath = path.resolve(
		siteWorkerRoot,
		'src/dynamic/bootstrap.ts',
	)
	const outfile = path.join(distDir, 'app-worker.js.txt')

	await esbuild.build({
		entryPoints: [bootstrapPath],
		outfile,
		bundle: true,
		format: 'esm',
		platform: 'node',
		target: 'es2022',
		conditions: [
			'workerd',
			'worker',
			'browser',
			'production',
			'module',
			'import',
			'default',
		],
		mainFields: ['module', 'main'],
		external: ['node:*', 'cloudflare:*'],
		alias: {
			...stubAliases,
		},
		define: sharedDefine,
		minify: true,
		sourcemap: false,
		logLevel: 'info',
		plugins: [
			{
				name: 'redirects-text',
				setup(build) {
					const redirectsContentsPromise = readFile(
						path.join(siteRoot, 'server/_redirects.txt'),
						'utf8',
					)
					build.onResolve({ filter: /_redirects\.txt$/ }, (args) => ({
						path: args.path,
						namespace: 'redirects-text',
					}))
					build.onLoad(
						{ filter: /.*/, namespace: 'redirects-text' },
						async () => ({
							contents: `export default ${JSON.stringify(
								await redirectsContentsPromise,
							)}`,
							loader: 'js',
						}),
					)
				},
			},
			{
				name: 'external-dynamic-imports',
				setup(build) {
					build.onResolve({ filter: /^site-content-data\.json$/ }, () => ({
						path: 'site-content-data.json',
						external: true,
					}))
					build.onResolve({ filter: /^mdx\// }, (args) => ({
						path: args.path,
						external: true,
					}))
				},
			},
		],
	})
	await patchAppWorkerNodeRequires(outfile)
	await patchAppWorkerWorkerdCompat(outfile)
}

async function reportBundleSizes() {
	const files = [
		path.join(distDir, 'app-worker.js.txt'),
		path.join(distDir, 'react-shim.js.txt'),
		path.join(distDir, 'react-jsx-runtime-shim.js.txt'),
	]
	const { gzipSync } = await import('node:zlib')
	for (const file of files) {
		const raw = await readFile(file)
		const gzip = gzipSync(raw)
		console.log(
			`${path.relative(siteWorkerRoot, file)}: ${raw.byteLength} bytes raw, ${gzip.byteLength} bytes gzip`,
		)
	}
}

async function main() {
	await ensureSiteBuild()
	await mkdir(distDir, { recursive: true })
	await buildAppWorkerBundle()
	await writeModuleMapShims()
	await reportBundleSizes()
	console.log('app-worker build complete')
}

main().catch((error) => {
	console.error(error)
	process.exit(1)
})
