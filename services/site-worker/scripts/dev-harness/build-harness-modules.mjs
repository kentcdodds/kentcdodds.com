import { readFile, readdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const harnessDir = __dirname
const siteWorkerRoot = path.resolve(harnessDir, '../..')
const distDir = path.resolve(siteWorkerRoot, 'dist')
const artifactsDir = path.resolve(harnessDir, 'artifacts')
const outFile = path.resolve(harnessDir, 'harness-modules.generated.mjs')

async function readText(filePath) {
	return readFile(filePath, 'utf8')
}

async function collectMdxModules() {
	const modules = {}
	const contentDirs = ['blog', 'pages']
	for (const contentDir of contentDirs) {
		const dir = path.join(artifactsDir, 'mdx-modules', contentDir)
		let entries = []
		try {
			entries = await readdir(dir)
		} catch {
			continue
		}
		for (const entry of entries) {
			if (!entry.endsWith('.js')) continue
			const slug = entry.replace(/\.js$/, '')
			modules[`mdx/${contentDir}/${slug}.js`] = await readText(
				path.join(dir, entry),
			)
		}
	}
	return modules
}

function addNestedReactShimAliases(modules, reactShim, jsxRuntimeShim) {
	const mdxPrefixes = new Set()
	for (const name of Object.keys(modules)) {
		if (!name.startsWith('mdx/') || !name.endsWith('.js')) continue
		const slash = name.lastIndexOf('/')
		if (slash > 0) mdxPrefixes.add(`${name.slice(0, slash + 1)}`)
	}
	for (const prefix of mdxPrefixes) {
		modules[`${prefix}react`] = reactShim
		modules[`${prefix}react/jsx-runtime`] = jsxRuntimeShim
	}
}

async function main() {
	const reactShim = await readText(path.join(distDir, 'react-shim.js.txt'))
	const jsxRuntimeShim = await readText(
		path.join(distDir, 'react-jsx-runtime-shim.js.txt'),
	)
	const modules = {
		'app-worker.js': await readText(path.join(distDir, 'app-worker.js.txt')),
		'site-content-data.js': await readText(
			path.join(artifactsDir, 'site-content-data.js'),
		),
		react: reactShim,
		'react/jsx-runtime': jsxRuntimeShim,
		...(await collectMdxModules()),
	}
	addNestedReactShimAliases(modules, reactShim, jsxRuntimeShim)

	const serialized = JSON.stringify(modules, null, 0)
	await writeFile(
		outFile,
		`// Generated by build-harness-modules.mjs — do not edit.\nexport const harnessModules = ${serialized}\n`,
	)
	console.log(`Wrote ${outFile}`)
}

main().catch((error) => {
	console.error(error)
	process.exit(1)
})
